def run_once(prev_scores=None, cycle_count=1, interval_min=2,
             top_alcistas=8, top_bajistas=5, debug=False):

    next_refresh = (
        datetime.datetime.now() + datetime.timedelta(minutes=interval_min)
    ).strftime("%H:%M:%S")

    clear_screen()

    print()
    print(C("  Descargando noticias en paralelo...", "yellow"), flush=True)

    t0 = time.time()
    raw_articles = fetch_all_parallel(debug=debug)
    elapsed = time.time() - t0

    all_articles = [analyze_article(a) for a in raw_articles]

    # Conteo por fuente
    src_counts = defaultdict(int)
    for a in all_articles: src_counts[a["source"]] += 1

    print_banner(cycle_count, interval_min, next_refresh,
                 len(all_articles), len(src_counts))

    # Resumen de fetch
    for src in SOURCES:
        col = SOURCES[src]["color"]
        n   = src_counts.get(src, 0)
        tag = C(f"OK ({n})", "green") if n > 0 else C("sin datos", "red")
        print(f"  {C(f'[{src}]', col):<30} {tag}")

    scores = aggregate_scores(all_articles)

    print_cockpit(scores, prev_scores)
    print_regional_heatmap(scores, prev_scores)
    print_sources(scores, prev_scores)
    print_topics(scores, prev_scores)
    if prev_scores:
        print_alerts(scores, prev_scores)
    print_geopolitics_section(all_articles, n=top_alcistas - 2)
    print_top_articles(all_articles, top_alcistas, top_bajistas)
    print_footer(all_articles, elapsed)

    return scores


def run_loop():
    prev_scores = None
    cycle_count = 0
    print(C("\n  ╔══════════════════════════════════════════════╗", "cyan"))
    print(C("  ║  NEWS SENTIMENT TERMINAL  v2.0  – MESA DIN  ║", "cyan"))
    print(C("  ╚══════════════════════════════════════════════╝\n", "cyan"))
    print(C(f"  Fuentes: {', '.join(SOURCES.keys())}", "dim"))
    print(C(f"  Modo: {'UNA_VEZ' if MODO_UNA_VEZ else f'LOOP cada {INTERVALO_MINUTOS} min'}", "yellow"))
    print(C(f"  Workers paralelos: {MAX_WORKERS}  │  Timeout/feed: {FEED_TIMEOUT}s\n", "dim"))

    while True:
        cycle_count += 1
        try:
            prev_scores = run_once(
                prev_scores  = prev_scores,
                cycle_count  = cycle_count,
                interval_min = INTERVALO_MINUTOS,
                top_alcistas = TOP_ALCISTAS,
                top_bajistas = TOP_BAJISTAS,
                debug        = DEBUG,
            )
        except KeyboardInterrupt:
            print(C("\n\n  Saliendo. Hasta luego.\n", "cyan"))
            break
        except Exception as e:
            # Loop blindado: NUNCA muere silenciosamente
            print(C(f"\n  [ERROR EN CICLO #{cycle_count}]: {e}  — reintentando en 30s...\n", "red"))
            if DEBUG: import traceback; traceback.print_exc()
            time.sleep(30)
            continue

        if MODO_UNA_VEZ:
            break

        try:
            for remaining in range(INTERVALO_MINUTOS * 60, 0, -1):
                mins = remaining // 60
                secs = remaining % 60
                print(C(f"\r  Próximo refresh en {mins:02d}:{secs:02d}  │  Ctrl+C para salir",
                        "dim"), end="", flush=True)
                time.sleep(1)
            print()
        except KeyboardInterrupt:
            print(C("\n\n  Saliendo. Hasta luego.\n", "cyan"))
            break


