def make_session():
    s = requests.Session()
    retry = Retry(total=2, backoff_factor=0.3, status_forcelist=[429,500,502,503,504])
    s.mount("http://",  HTTPAdapter(max_retries=retry))
    s.mount("https://", HTTPAdapter(max_retries=retry))
    s.headers.update({
        "User-Agent": (
            "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
            "AppleWebKit/537.36 Chrome/122.0.0.0 Safari/537.36"
        ),
        "Accept-Language": "en-US,en;q=0.9,es;q=0.8",
        "Accept": "application/rss+xml, application/xml, text/xml, */*",
    })
    return s

SESSION = make_session()


def fetch_single_feed(args):
    """Worker para ThreadPoolExecutor. Retorna lista de artículos o []."""
    url, src_name, lang, debug = args
    articles = []
    try:
        if HAS_FEEDPARSER:
            feed = feedparser.parse(url, request_headers={
                "User-Agent": SESSION.headers["User-Agent"]
            })
            for entry in feed.entries:
                title   = getattr(entry, "title",    "") or ""
                summary = getattr(entry, "summary",  "") or ""
                link    = getattr(entry, "link",     "") or ""
                pub     = getattr(entry, "published","") or getattr(entry,"updated","") or ""
                content = ""
                if hasattr(entry, "content"):
                    try:
                        content = entry.content[0].get("value","") or ""
                        content = re.sub(r"<[^>]+>", " ", content)[:400]
                    except Exception:
                        pass
                if title.strip():
                    clean_summary = re.sub(r"<[^>]+>", " ", summary).strip()[:300]
                    articles.append({
                        "title":   title.strip(),
                        "summary": clean_summary,
                        "content": content.strip(),
                        "link":    link.strip(),
                        "pub":     str(pub)[:30],
                        "source":  src_name,
                        "lang":    lang,
                    })
        else:
            import xml.etree.ElementTree as ET
            resp = SESSION.get(url, timeout=FEED_TIMEOUT)
            resp.raise_for_status()
            root = ET.fromstring(resp.content)
            for item in root.findall(".//item")[:25]:
                def txt(tag):
                    el = item.find(tag)
                    return (el.text or "").strip() if el is not None else ""
                t = txt("title")
                if t:
                    articles.append({
                        "title":   t,
                        "summary": re.sub(r"<[^>]+>", " ", txt("description"))[:300],
                        "content": "",
                        "link":    txt("link"),
                        "pub":     txt("pubDate")[:30],
                        "source":  src_name,
                        "lang":    lang,
                    })
    except Exception as e:
        if debug:
            print(C(f"  [RSS ERR] {src_name} {url[:55]}: {e}", "dim"))
    return articles


