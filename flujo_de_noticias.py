def weighted_avg(scores):
    signal = [s for s in scores if abs(s) > 0.5]
    if not signal: return 0.0
    weights = [abs(s) for s in signal]
    tw = sum(weights)
    if tw == 0: return round(sum(signal)/len(signal), 1)
    return round(sum(s*w for s,w in zip(signal,weights))/tw, 1)

def aggregate_scores(articles):
    if not articles: return {}
    all_s = [a["sentiment_score"] for a in articles]
    result = {"general": weighted_avg(all_s)}

    by_src = defaultdict(list)
    by_top = defaultdict(list)
    by_reg = defaultdict(list)
    by_geo = []

    for a in articles:
        by_src[a["source"]].append(a["sentiment_score"])
        for t in a.get("topics",[]): by_top[t].append(a["sentiment_score"])
        for r in a.get("regions",[]): by_reg[r].append(a["sentiment_score"])
        if is_geopolitical(a): by_geo.append(a["sentiment_score"])

    result["by_source"]  = {k: weighted_avg(v) for k, v in by_src.items()}
    result["by_topic"]   = {k: weighted_avg(v) for k, v in by_top.items()}
    result["by_region"]  = {k: weighted_avg(v) for k, v in by_reg.items()}
    result["geopolitics_score"] = weighted_avg(by_geo) if by_geo else None

    pos = sum(1 for a in articles if a["sentiment_label"]=="positive")
    neg = sum(1 for a in articles if a["sentiment_label"]=="negative")
    neu = sum(1 for a in articles if a["sentiment_label"]=="neutral")
    tot = len(articles)
    result["distribution"] = {
        "positive":pos,"negative":neg,"neutral":neu,"total":tot,
        "bull_ratio":round(pos/tot*100,1) if tot else 0,
        "bear_ratio":round(neg/tot*100,1) if tot else 0,
    }
    by_lang = defaultdict(list)
    for a in articles:
        by_lang[a.get("lang_detected","?")].append(a["sentiment_score"])
    result["by_lang"] = {k: round(sum(v)/len(v),1) for k,v in by_lang.items()}

    return result


