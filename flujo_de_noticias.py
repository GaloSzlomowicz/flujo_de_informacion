    all_articles = []
    seen         = set()

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as ex:
        futures = {ex.submit(fetch_single_feed, t): t for t in tasks}
        for fut in as_completed(futures, timeout=FEED_TIMEOUT + 2):
            try:
                arts = fut.result()
                for a in arts:
                    key = a["title"][:70].lower()
                    if key and key not in seen:
                        seen.add(key)
                        all_articles.append(a)
            except Exception as e:
                if debug:
                    print(C(f"  [FUTURE ERR] {e}", "dim"))

    return all_articles


