def C(text, col):
    if not HAS_COLOR: return str(text)
    return _CMAP.get(col, "") + str(text) + Style.RESET_ALL

def score_bar(score, width=24):
    norm = max(-100.0, min(100.0, float(score)))
    half = width // 2
    fill = min(int(abs(norm) / 100.0 * half), half)
    mid  = C("|", "dim")
    if norm >= 0:
        return C("·" * half, "dim") + mid + C("█" * fill, "green") + C("·" * (half - fill), "dim")
    return C("·" * (half - fill), "dim") + C("█" * fill, "red") + mid + C("·" * half, "dim")

def momentum_bar(delta, width=10):
    """Barra de velocidad de cambio (momentum)."""
    fill = min(int(abs(delta) / 50.0 * width), width)
    col  = "green" if delta >= 0 else "red"
    arrow = "▲" if delta > 0 else ("▼" if delta < 0 else "─")
    return C(arrow + "█" * fill + "·" * (width - fill), col)

def sentiment_label(score):
    s = float(score)
    if s >= 50:   return C("ALCISTA FUERTE ▲▲", "green")
    if s >= 20:   return C("Alcista        ▲ ", "green")
    if s >= 5:    return C("Leve alcista   △ ", "green")
    if s > -5:    return C("Neutral        ─ ", "yellow")
    if s > -20:   return C("Leve bajista   ▽ ", "red")
    if s > -50:   return C("Bajista        ▼ ", "red")
    return               C("BAJISTA FUERTE ▼▼", "red")

def risk_flag(score):
    s = float(score)
    if s >= 40:  return C("[RISK-ON]",  "bgreen")
    if s >= 10:  return C("[+BIAS]",    "green")
    if s > -10:  return C("[NEUTRAL]",  "yellow")
    if s > -40:  return C("[-BIAS]",    "red")
    return              C("[RISK-OFF]", "bred")

def delta_str(delta):
    d = float(delta)
