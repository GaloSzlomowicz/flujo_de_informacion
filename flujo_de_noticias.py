_ES_MARKERS = {"de","la","el","los","las","que","en","con","por","del",
               "una","un","es","se","su","al","como","mas","pero","y","o",
               "para","esta","esto","sus","una","precio","banco","peso"}

def detect_language(text):
    words = text.lower().split()
    return "es" if sum(1 for w in words if w in _ES_MARKERS) >= 3 else "en"

def normalize(text):
    t = text.lower()
    for s, d in [("á","a"),("é","e"),("í","i"),("ó","o"),("ú","u"),("ü","u"),("ñ","n")]:
        t = t.replace(s, d)
    return re.sub(r"[^\w\s]", " ", t)

def score_text(text, lang="auto"):
    if not text: return 0.0
    if lang == "auto": lang = detect_language(text)
    tokens = normalize(text).split()
    total  = 0.0
    for i, tok in enumerate(tokens):
        if tok not in LEXICON: continue
        base      = float(LEXICON[tok])
        negated   = any(tokens[j] in NEGATORS   for j in range(max(0, i-3), i))
        amplified = any(tokens[j] in AMPLIFIERS for j in range(max(0, i-2), i))
        score     = base * (1.6 if amplified else 1.0)
        if negated: score = -score
        total += score
    wc = max(len(tokens), 1)
    return max(-100.0, min(100.0, (total / (wc ** 0.5)) * 22.0))

def classify_topics(text):
    tl = normalize(text)
    return [t for t, kw in TOPIC_KEYWORDS.items()
            if any(normalize(k) in tl for k in kw.get("en",[]) + kw.get("es",[]))]

def classify_region(text):
    tl = text.lower()
    return [r for r, kws in REGION_KEYWORDS.items() if any(k in tl for k in kws)]

def is_geopolitical(article):
    return "geopolitics" in article.get("topics", [])

def analyze_article(article):
    full = article["title"] + " " + article.get("summary"," স্বেচ্ছাসেবничество") + " " + article.get("content"," স্বেচ্ছাসেবничество")
    lang = article.get("lang","auto")
    if lang not in ("en","es"): lang = detect_language(full)
    score   = score_text(full, lang=lang)
    topics  = classify_topics(full)
    regions = classify_region(full)
    return {
        **article,
        "sentiment_score": round(score, 1),
        "sentiment_label": ("positive" if score >= 15
                             else ("negative" if score <= -15 else "neutral")),
        "topics":          topics,
        "regions":         regions,
        "lang_detected":   lang,
    }


