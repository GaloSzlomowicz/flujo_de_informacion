def print_alerts(scores, prev):
    if not prev: return
    alerts = []
    d = scores.get("general",0) - prev.get("general",0)
    if abs(d) >= 18: alerts.append(("GENERAL", d))
    for src, s in scores.get("by_source",{}).items():
        dd = s - prev.get("by_source",{}).get(src,s)
        if abs(dd) >= 22: alerts.append((src, dd))
    for topic, s in scores.get("by_topic",{}).items():
        dd = s - prev.get("by_topic",{}).get(topic,s)
        if abs(dd) >= 22: alerts.append((TOPIC_LABELS.get(topic,topic), dd))
    for reg, s in scores.get("by_region",{}).items():
        dd = s - prev.get("by_region",{}).get(reg,s)
        if abs(dd) >= 22: alerts.append((f"REGION:{reg}", dd))
    if alerts:
        print()
        print(C("!!" * 43 + "!!", "red"))
        print(C("  !! ALERTAS DE CAMBIO BRUSCO DE SENTIMIENTO", "red"))
        for name, delta in alerts:
            direction = "DETERIORO BRUSCO ▼" if delta < 0 else "MEJORA BRUSCA ▲"
            col = "red" if delta < 0 else "green"
            print(C(f"  !!  {name:<30}  {direction}  Delta = {delta:+.1f}", col))
        print(C("!!" * 43 + "!!", "red"))


